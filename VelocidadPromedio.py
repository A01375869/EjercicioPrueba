# Calcula la velocidad promedio en un viaje

tiempo = int(input("Escribe el tiempo de viaje en hrs. enteras: "))
distancia = int(input("Escribe la distancia de viaje en km. enteros: "))

velocidad = distancia/tiempo

print("La velocidad promedio es:", velocidad, "km/h")